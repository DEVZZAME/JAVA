package ex3_continue;

public class Ex2_continue {

	public static void main(String[] args) {

		for (int i = 1; i <= 3; i++) {
			switch (i) {
			case 1:
				System.out.println("111111");
				continue;
				
			case 2:
				System.out.println("222222");
				break;
				
			}// switch
			
		} // for

	}// main

}
